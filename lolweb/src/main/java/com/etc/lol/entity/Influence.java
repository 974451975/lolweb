package com.etc.lol.entity;

public class Influence {
    private Integer influence_id;
    private String influence_name;
    private String influence_logo;
    private String influence_text;

    public Integer getInfluence_id() {
        return influence_id;
    }

    public void setInfluence_id(Integer influence_id) {
        this.influence_id = influence_id;
    }

    public String getInfluence_name() {
        return influence_name;
    }

    public void setInfluence_name(String influence_name) {
        this.influence_name = influence_name;
    }

    public String getInfluence_logo() {
        return influence_logo;
    }

    public void setInfluence_logo(String influence_logo) {
        this.influence_logo = influence_logo;
    }

    public String getInfluence_text() {
        return influence_text;
    }

    public void setInfluence_text(String influence_text) {
        this.influence_text = influence_text;
    }
}
