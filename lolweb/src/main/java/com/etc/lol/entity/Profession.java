package com.etc.lol.entity;

public class Profession {
    private Integer profession_id;
    private String profession_name;
    private String profession_logo;

    public Integer getProfession_id() {
        return profession_id;
    }

    public void setProfession_id(Integer profession_id) {
        this.profession_id = profession_id;
    }

    public String getProfession_name() {
        return profession_name;
    }

    public void setProfession_name(String profession_name) {
        this.profession_name = profession_name;
    }

    public String getProfession_logo() {
        return profession_logo;
    }

    public void setProfession_logo(String profession_logo) {
        this.profession_logo = profession_logo;
    }
}
